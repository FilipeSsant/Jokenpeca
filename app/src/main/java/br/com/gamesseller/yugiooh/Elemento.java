public class Elemento {

    private String nomeElemento;
    private int idElemento;
    private int imagemElemento;

    public String getNomeElemento() {
        return nomeElemento;
    }

    public void setNomeElemento(String nomeElemento) {
        this.nomeElemento = nomeElemento;
    }

    public int getIdElemento() {
        return idElemento;
    }

    public void setIdElemento(int idElemento) {
        this.idElemento = idElemento;
    }

    public int getImagemElemento() {
        return imagemElemento;
    }

    public void setImagemElemento(int imagemElemento) {
        this.imagemElemento = imagemElemento;
    }
}
